                            <p><b>PROCESSING OF PERSONAL DATA</b></p>
                            <p>
                                APC adheres to the general principles of transparency,
                                legitimate purpose, and proportionality in the processing of
                                employee personal data and information (Rule IV. Sec. 18, IRR,
                                DPA of 2012).
                            </p>
                            <p><b>COLLECTION OF PERSONAL DATA</b></p>
                            <p>
                                The College has the right to collect pertinent data and
                                information of members of the APC community as it deems
                                necessary and important, provided that the DPA of 2012 is not
                                violated, following the general principles in collection,
                                processing, and retention.
                            </p>
                            <p>
                                <span style="font-size: 16px">1. IRR of the DPA of 2012, Rule IV. Data Privacy
                                    Principles, Sec. 19: “Collection must be for a specified and
                                    legitimate purpose</span><br /><span style="font-size: 16px">2. Only authorized
                                    personnel of the college shall be
                                    allowed to collect personal data.</span><br /><span style="font-size: 16px">3.
                                    Purpose of Personal Data Collection (Employee):</span>
                            </p>
                            <p>
                                a. APC uses employee data for personnel administration, work,
                                and general management purposes. For example, APC needs this
                                information to administer compensation and benefits,
                                performance evaluation and reviews, learning and development,
                                promotion and succession planning, and to maintain efficient
                                and effective records management.
                            </p>
                            <p>
                                b. APC may also use employee information in confidential
                                references in connection with applications for employment or
                                further education or financial references; where an employee
                                is undertaking further study which the College has paid for,
                                or made a financial contribution toward an employee’s
                                professional growth, development, and achievement.
                            </p>
                            <p>
