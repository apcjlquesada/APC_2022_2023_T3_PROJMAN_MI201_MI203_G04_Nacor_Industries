                            <p><b>RECORDS KEEPING and RETENTION</b></p>
                            <p>
                                1. IRR of the DPA of 2012, Rule IV. Data Privacy Principles,
                                Sec. 19: “Personal Data shall not be retained longer than
                                necessary.
                            </p>
                            <p>
                                2. The personnel records and data shall be stored inside the
                                HR office or an approved secure storage facility off-site.
                            </p>
                            <p>
                                3. Official personnel and other records shall only be retained
                                and stored in the HR Office for a maximum of five (5) years.
                                All documents, which have collected and stored in the HR
                                Office beyond the prescribed period, shall be disposed
                                immediately
                            </p>
